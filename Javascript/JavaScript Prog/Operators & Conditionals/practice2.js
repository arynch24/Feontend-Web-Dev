let m=prompt("Enter your score");
if(m<=100 && m>=90)
{
    console.log("A");
}
else if(m<=89 && m>=80)
{
    console.log("B");
}
else if(m<=79 && m>=70)
{
    console.log("C");
}
else if(m<=69 && m>=60)
{
    console.log("D");
}
else if(m<=59 && m>=0)
{
    console.log("E");
}
else
{
    console.log("Invalid Input");
}
